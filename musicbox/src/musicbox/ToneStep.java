package musicbox;

/**
 * Definition of valid tone steps.
 * W - Whole note step (e.g. from C to D).
 * H - Half note step (e.g. from C to C#).
 * WH - Whole and a half note step (e.g. from C to D#).
 * 
 * @author Michael Strasser
 */
public enum ToneStep {
    W,
    H,
    WH
}
